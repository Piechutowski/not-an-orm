// Package sqlite generates SQLite DDL (nao_schema.sql) from a checked
// DBML file: CREATE TABLE per table, CREATE INDEX per non-pk index, and
// INSERT statements from records.
//
// Dialect decisions (see SPEC.md):
//   - enums become TEXT + CHECK (col IN (...)) — SQLite has no enum type;
//   - schemas are flattened: core.users -> core_users;
//   - every primary-key column gets an explicit NOT NULL, because SQLite's
//     PRIMARY KEY famously does not imply NOT NULL on its own;
//   - an integer [pk, increment] column becomes exactly INTEGER PRIMARY KEY,
//     the rowid alias (AUTOINCREMENT is deliberately not used);
//   - one-to-one relationships add UNIQUE on the FK column when nothing
//     else guarantees it; many-to-many (<>) generates nothing;
//   - backtick expressions pass through verbatim: they are dialect-owned.
//
// DBML notes become "--" comments above the element they document, at
// every level: project, table, column.
package sqlite

import (
	"fmt"
	"sort"
	"strings"

	"github.com/Piechutowski/not-an-orm/edbml/ast"
	"github.com/Piechutowski/not-an-orm/edbml/check"
	"github.com/Piechutowski/not-an-orm/edbml/parser"
	"github.com/Piechutowski/not-an-orm/edbml/token"
)

// Options configures generation.
type Options struct {
	// Source is the name of the DBML input, recorded in the header.
	Source string
}

// Generate renders the schema file for one checked DBML file.
func Generate(f *ast.File, info *check.Info, opts Options) ([]byte, error) {
	g := &generator{f: f, info: info, opts: opts}
	if err := g.run(); err != nil {
		return nil, err
	}
	return []byte(g.out.String()), nil
}

type generator struct {
	f    *ast.File
	info *check.Info
	opts Options

	enums map[string]*check.EnumInfo // canonical key -> enum
	out   strings.Builder
}

func (g *generator) run() error {
	g.enums = map[string]*check.EnumInfo{}
	for _, e := range g.info.Enums {
		g.enums[e.Key] = e
	}

	fmt.Fprintf(&g.out, "-- Code generated by nao gen sqlite from %s; DO NOT EDIT.\n", g.opts.Source)
	if note := projectNote(g.f); note != "" {
		g.out.WriteString("--\n")
		writeComment(&g.out, "", note)
	}
	g.out.WriteString("\nPRAGMA foreign_keys = ON;\n")

	seen := map[string]string{}
	for _, ti := range g.info.Tables {
		if err := g.emitTable(ti, seen); err != nil {
			return err
		}
	}

	// Seed data last, in source order: all tables exist by then.
	for _, ti := range g.info.Tables {
		for _, item := range ti.Decl.Body {
			if r, ok := item.(*ast.Records); ok {
				if err := g.emitInserts(ti, r); err != nil {
					return err
				}
			}
		}
	}
	for _, d := range g.f.Decls {
		if r, ok := d.(*ast.Records); ok && r.Table != nil {
			ti := g.tableByName(r.Table)
			if ti == nil {
				return fmt.Errorf("records target %q not found", r.Table.String())
			}
			if err := g.emitInserts(ti, r); err != nil {
				return err
			}
		}
	}
	return nil
}

func (g *generator) tableByName(q *ast.QualName) *check.TableInfo {
	for _, ti := range g.info.Tables {
		if ti.Key == canonKey(q) || (ti.Alias != "" && q.Schema() == "" && ti.Alias == q.Base()) {
			return ti
		}
	}
	return nil
}

func canonKey(q *ast.QualName) string {
	if q.Schema() == "" {
		return "public." + q.Base()
	}
	return q.String()
}

// sqlTableName flattens the schema qualifier: core.users -> core_users.
func sqlTableName(q *ast.QualName) string {
	if s := q.Schema(); s != "" && s != "public" {
		return s + "_" + q.Base()
	}
	return q.Base()
}

/* ===== CREATE TABLE ===== */

func (g *generator) emitTable(ti *check.TableInfo, seen map[string]string) error {
	name := sqlTableName(ti.Decl.Name)
	if prev, dup := seen[name]; dup {
		return fmt.Errorf("tables %s and %s both flatten to SQLite name %q", prev, ti.Decl.Name.String(), name)
	}
	seen[name] = ti.Decl.Name.String()

	g.out.WriteString("\n")
	if note := tableNote(ti.Decl); note != "" {
		writeComment(&g.out, "", note)
	}
	fmt.Fprintf(&g.out, "CREATE TABLE %s (\n", quoteIdent(name))

	pkCols := compositePKColumns(ti)
	uniqueDone := map[string]bool{} // column sets already unique, for 1:1 dedupe

	var lines []string
	for _, cd := range ti.Columns {
		line, err := g.columnDef(ti, cd, pkCols)
		if err != nil {
			return fmt.Errorf("table %s: %w", ti.Decl.Name.String(), err)
		}
		if note := settingNote(cd.Col.Settings); note != "" {
			var b strings.Builder
			writeComment(&b, "  ", note)
			line = b.String() + line
		}
		lines = append(lines, line)
		if columnHas(cd, "unique") || (columnHas(cd, "pk") && len(pkCols) == 0) {
			uniqueDone[cd.Col.Name.Name()] = true
		}
	}

	// composite (or index-declared) primary key
	if len(pkCols) > 0 {
		lines = append(lines, "  PRIMARY KEY ("+quoteJoin(pkCols)+")")
		uniqueDone[strings.Join(pkCols, ",")] = true
	}

	// table-level checks (spec §6.6)
	for _, item := range ti.Decl.Body {
		cb, ok := item.(*ast.ChecksBlock)
		if !ok {
			continue
		}
		for _, ck := range cb.Checks {
			line := "  "
			if n := settingString(ck.Settings, "name"); n != "" {
				line += "CONSTRAINT " + quoteIdent(n) + " "
			}
			line += "CHECK (" + ck.Expr.Text() + ")"
			lines = append(lines, line)
		}
	}

	// foreign keys from all three ref forms (spec §6.7)
	for _, r := range g.info.Refs {
		fk, target, ok := fkSides(r)
		if !ok || fk.Table != ti || target.Table == nil {
			continue
		}
		line := "  FOREIGN KEY (" + quoteJoin(fk.Columns) + ") REFERENCES " +
			quoteIdent(sqlTableName(target.Table.Decl.Name)) + " (" + quoteJoin(target.Columns) + ")"
		if a := refAction(r, "delete"); a != "" {
			line += " ON DELETE " + strings.ToUpper(a)
		}
		if a := refAction(r, "update"); a != "" {
			line += " ON UPDATE " + strings.ToUpper(a)
		}
		lines = append(lines, line)

		// one-to-one: the FK side must be unique for the cardinality to hold
		if r.Op == token.MINUS && !g.alreadyUnique(ti, fk.Columns, uniqueDone) {
			lines = append(lines, "  UNIQUE ("+quoteJoin(fk.Columns)+")")
			uniqueDone[strings.Join(fk.Columns, ",")] = true
		}
	}

	g.out.WriteString(strings.Join(lines, ",\n"))
	g.out.WriteString("\n);\n")

	return g.emitIndexes(ti)
}

func (g *generator) columnDef(ti *check.TableInfo, cd *check.ColumnDef, pkCols []string) (string, error) {
	col := cd.Col
	colName := col.Name.Name()

	sqlType, enumInfo, err := g.resolveType(col)
	if err != nil {
		return "", fmt.Errorf("column %q: %w", colName, err)
	}

	singlePK := columnHas(cd, "pk") && len(pkCols) == 0
	inPK := singlePK || contains(pkCols, colName)
	rowidAlias := singlePK && sqlType == "INTEGER" && columnHas(cd, "increment")

	parts := []string{"  " + quoteIdent(colName), sqlType}
	if singlePK {
		parts = append(parts, "PRIMARY KEY")
	}
	// SQLite quirk: PRIMARY KEY does not imply NOT NULL (except the rowid
	// alias), so pk membership always gets an explicit NOT NULL.
	if !rowidAlias && (inPK || !isNullable(col)) {
		parts = append(parts, "NOT NULL")
	}
	if columnHas(cd, "unique") {
		parts = append(parts, "UNIQUE")
	}
	if s := col.Settings.Get("default"); s != nil {
		def, err := defaultSQL(s.Value)
		if err != nil {
			return "", fmt.Errorf("column %q: %w", colName, err)
		}
		parts = append(parts, "DEFAULT "+def)
	}
	if enumInfo != nil {
		var vals []string
		for _, v := range enumInfo.Decl.Values {
			vals = append(vals, quoteString(v.Name.Name()))
		}
		parts = append(parts, "CHECK ("+quoteIdent(colName)+" IN ("+strings.Join(vals, ", ")+"))")
	}
	if col.Settings != nil {
		for _, s := range col.Settings.Settings {
			if s.Name != "check" {
				continue
			}
			if fe, ok := s.Value.(*ast.FuncExpr); ok {
				parts = append(parts, "CHECK ("+fe.Text()+")")
			}
		}
	}
	return strings.Join(parts, " "), nil
}

/* ===== indexes ===== */

func (g *generator) emitIndexes(ti *check.TableInfo) error {
	tbl := sqlTableName(ti.Decl.Name)
	used := map[string]bool{}
	n := 0
	for _, ix := range ti.Indexes {
		if ix.Settings.Get("pk") != nil {
			continue // emitted as the PRIMARY KEY constraint
		}
		n++
		name := settingString(ix.Settings, "name")
		if name == "" {
			name = fmt.Sprintf("idx_%s_%d", tbl, n)
		}
		if used[name] {
			return fmt.Errorf("table %s: duplicate index name %q", ti.Decl.Name.String(), name)
		}
		used[name] = true

		stmt := "CREATE INDEX"
		if ix.Settings.Get("unique") != nil {
			stmt = "CREATE UNIQUE INDEX"
		}
		var keys []string
		for _, atom := range ix.Key {
			switch a := atom.(type) {
			case *ast.Ident:
				keys = append(keys, quoteIdent(a.Name()))
			case *ast.FuncExpr:
				keys = append(keys, "("+a.Text()+")")
			}
		}
		fmt.Fprintf(&g.out, "%s %s ON %s (%s);\n", stmt, quoteIdent(name), quoteIdent(tbl), strings.Join(keys, ", "))
	}
	return nil
}

/* ===== records -> INSERT ===== */

func (g *generator) emitInserts(ti *check.TableInfo, r *ast.Records) error {
	cols := make([]string, 0)
	if r.HasColumns {
		for _, c := range r.Columns {
			cols = append(cols, c.Name())
		}
	} else {
		for _, cd := range ti.Columns {
			cols = append(cols, cd.Col.Name.Name())
		}
	}
	if len(r.Rows) == 0 {
		return nil
	}
	fmt.Fprintf(&g.out, "\nINSERT INTO %s (%s) VALUES\n", quoteIdent(sqlTableName(ti.Decl.Name)), quoteJoin(cols))
	var rows []string
	for _, row := range r.Rows {
		var vals []string
		for _, v := range row.Values {
			sql, err := valueSQL(v)
			if err != nil {
				return fmt.Errorf("records for %s: %w", ti.Decl.Name.String(), err)
			}
			vals = append(vals, sql)
		}
		rows = append(rows, "  ("+strings.Join(vals, ", ")+")")
	}
	g.out.WriteString(strings.Join(rows, ",\n"))
	g.out.WriteString(";\n")
	return nil
}

/* ===== value rendering ===== */

// defaultSQL renders a §6.4 default value; expressions are parenthesized
// as SQLite requires for non-literal defaults.
func defaultSQL(v ast.Node) (string, error) {
	switch v := v.(type) {
	case *ast.FuncExpr:
		return "(" + v.Text() + ")", nil
	default:
		return valueSQL(v)
	}
}

func valueSQL(v ast.Node) (string, error) {
	switch v := v.(type) {
	case *ast.BasicLit:
		if v.Tok.Kind == token.STRING {
			return quoteString(v.Tok.Val), nil
		}
		return v.Tok.Val, nil // number
	case *ast.NegNumber:
		return "-" + v.Num.Tok.Val, nil
	case *ast.FuncExpr:
		return v.Tok.Val, nil
	case *ast.EnumConst:
		return quoteString(v.Value.Name()), nil
	case *ast.Empty:
		return "NULL", nil
	case *ast.Ident:
		switch strings.ToLower(v.Name()) {
		case "true":
			return "1", nil
		case "false":
			return "0", nil
		case "null":
			return "NULL", nil
		}
	}
	return "", fmt.Errorf("unsupported value at %s", v.Pos())
}

/* ===== semantic helpers (mirroring the spec rules) ===== */

func compositePKColumns(ti *check.TableInfo) []string {
	var out []string
	for _, ix := range ti.Indexes {
		if ix.Settings.Get("pk") == nil {
			continue
		}
		for _, atom := range ix.Key {
			if id, ok := atom.(*ast.Ident); ok {
				out = append(out, id.Name())
			}
		}
	}
	return out
}

func isNullable(col *ast.Column) bool {
	s := col.Settings
	if s != nil {
		if s.Get("not null") != nil || s.Get("pk") != nil || s.Get("primary key") != nil || s.Get("increment") != nil {
			return false
		}
	}
	for _, f := range col.LegacyFlags {
		if strings.EqualFold(f.Name(), "pk") {
			return false
		}
	}
	return true
}

func columnHas(cd *check.ColumnDef, name string) bool {
	if cd.Col.Settings != nil {
		if cd.Col.Settings.Get(name) != nil {
			return true
		}
		if name == "pk" && cd.Col.Settings.Get("primary key") != nil {
			return true
		}
	}
	for _, f := range cd.Col.LegacyFlags {
		if strings.EqualFold(f.Name(), name) {
			return true
		}
	}
	return false
}

// fkSides mirrors spec §6.7.3: which endpoint carries the foreign key.
func fkSides(r *check.RefInfo) (fk, target check.EndpointInfo, ok bool) {
	switch r.Op {
	case token.GT:
		return r.Left, r.Right, true
	case token.LT:
		return r.Right, r.Left, true
	case token.MINUS:
		return r.Right, r.Left, true
	default:
		return fk, target, false
	}
}

func refAction(r *check.RefInfo, kind string) string {
	ref, ok := r.Node.(*ast.Ref)
	if !ok || ref.Settings == nil {
		return ""
	}
	s := ref.Settings.Get(kind)
	if s == nil {
		return ""
	}
	switch v := s.Value.(type) {
	case *ast.Ident:
		return v.Name()
	case parser.MultiWord:
		return strings.Join(v.Words(), " ")
	}
	return ""
}

func (g *generator) alreadyUnique(ti *check.TableInfo, cols []string, done map[string]bool) bool {
	if done[strings.Join(cols, ",")] {
		return true
	}
	if len(cols) == 1 {
		if cd := ti.Column(cols[0]); cd != nil && (columnHas(cd, "unique") || columnHas(cd, "pk")) {
			return true
		}
	}
	want := append([]string(nil), cols...)
	sort.Strings(want)
	for _, ix := range ti.Indexes {
		if ix.Settings.Get("pk") == nil && ix.Settings.Get("unique") == nil {
			continue
		}
		var got []string
		for _, atom := range ix.Key {
			if id, ok := atom.(*ast.Ident); ok {
				got = append(got, id.Name())
			}
		}
		sort.Strings(got)
		if strings.Join(got, ",") == strings.Join(want, ",") {
			return true
		}
	}
	return false
}

/* ===== notes ===== */

func projectNote(f *ast.File) string {
	for _, d := range f.Decls {
		if p, ok := d.(*ast.Project); ok && len(p.Notes) > 0 {
			return p.Notes[len(p.Notes)-1].Text.Tok.Val
		}
	}
	return ""
}

func tableNote(t *ast.Table) string {
	for _, item := range t.Body {
		if n, ok := item.(*ast.Note); ok {
			return n.Text.Tok.Val
		}
	}
	return settingNote(t.Settings)
}

func settingNote(sl *ast.SettingList) string { return settingString(sl, "note") }

func settingString(sl *ast.SettingList, name string) string {
	s := sl.Get(name)
	if s == nil {
		return ""
	}
	if lit, ok := s.Value.(*ast.BasicLit); ok {
		return lit.Tok.Val
	}
	return ""
}

func writeComment(b *strings.Builder, indent, text string) {
	for _, line := range strings.Split(strings.TrimRight(text, "\n"), "\n") {
		if strings.TrimSpace(line) == "" {
			b.WriteString(indent + "--\n")
			continue
		}
		b.WriteString(indent + "-- " + line + "\n")
	}
}

func contains(ss []string, s string) bool {
	for _, x := range ss {
		if x == s {
			return true
		}
	}
	return false
}
